const processProjects = (level: 'beginner' | 'intermediate' | 'advanced', data: ProjectData): { [category: string]: Project[] } => {
    let idCounter = 1;
    const result: { [category: string]: Project[] } = {};
    for (const category in data) {
        result[category] = data[category].map((proj) => ({
            ...proj,
            concepts: [], // Add empty concepts array
            id: `${level}-${idCounter++}`,
            difficulty: level,
            category: category,
            github: "#",
            demo: "#",
        }));
    }
    return result;
};

